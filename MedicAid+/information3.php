I'm Not Sure I'm Eligible:<br>
During a Power Red donation, you give a concentrated dose of red cells, the part of your blood used every day for those needing transfusions as part of their care. This type of donation uses an automated process that separates your red blood cells from the other blood components, and then safely and comfortably returns your plasma and platelets to you. <br>

With just a little extra time at your appointment, you can donate more red cells and increase your impact on patients in need. Learn more about Power Red donations.
Who it helps: Red cells from a Power Red donation are typically given to trauma patients, newborns and emergency transfusions during birth, people with sickle cell anemia, and anyone suffering blood loss.<br>

Time it takes: About 1.5 hours<br>

Ideal blood types: O positive, O negative, A negative, and B negative<br>

Donation frequency: Every 112 days, up to 3 times/year. <br>
<br>
<br>
<br>
Platelet Donation: <br>
Platelets are tiny cells in your blood that form clots and stop bleeding. Platelets are most often used by cancer patients and others facing life-threatening illnesses and injuries.<br>

In a platelet donation, an apheresis machine collects your platelets along with some plasma, returning your red cells and most of the plasma back to you. A single donation of platelets can yield several transfusable units, whereas it takes about five whole blood donations to make up a single transfusable unit of platelets.<br>

Platelets are collected at Red Cross donation centers only, and are not collected at blood drives. Learn more about platelet donations.<br>
Who it helps: Platelets are a vital element of cancer treatments and organ transplant procedures, as well as other surgical procedures.<br>
Time it takes: About 2.5-3 hours<br>

Ideal blood types: A positive, A negative, B positive, O positive, AB positive and AB negative<br>

Donation frequency: Every 7 days, up to 24 times/year<br>
<br>
<br>
<br>
Plasma Donation:<br>
During an AB Elite donation, you give plasma, a part of your blood used to treat patients in emergency situations. AB plasma can be given to anyone regardless of their blood type. Plasma is collected through an automated process that separates plasma from other blood components, then safely and comfortably returns your red blood cells and platelets to you. AB Elite maximizes your donation and takes just a few minutes longer than donating blood.<br>

Plasma is collected at select Red Cross donation centers. Learn more about plasma donation.<br>
Who it helps: AB Plasma is used in emergency and trauma situations to help stop bleeding. <br>

Time it takes: About 1 hour and 15 minutes<br>

Ideal blood types: AB positive, AB negative<br>

Donation frequency: Every 28 days, up to 13 times/year<br>
