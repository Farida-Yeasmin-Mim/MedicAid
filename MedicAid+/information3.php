<p style="color: blue;">I'm Not Sure I'm Eligible:<br></p>
<p>
Approximately 38% of Americans are eligible to donate blood at any given time, so there’s a very good chance you meet the criteria.For most donations, these are the basic requirements: Good Health: <br>
You generally feel well, even if you're being treated for a chronic condition. <br>
<i class="far fa-hand-point-right"></i>Age 17 <br>
16 year olds may donate with parental permission in certain states if allowed by state law.<br>
<i class="far fa-hand-point-right"></i>110 lbs<br>
Additional weight requirements apply for donors age 18 or younger.
</p>
<br>
<br>
<br>
<p style="color: blue;">Is it Safe to Donate?: <br></p>
<p>
<i class="far fa-hand-point-right"></i>Most donors feel fine after donating blood, but if you do feel faint or fatigued, simply lie down until you feel better.<br>
<br>
<i class="far fa-hand-point-right"></i>It might help you to think about this: you have about 10 pints of blood in your body. Roughly 1 pint is given during a donation.<br>
<br>
<i class="far fa-hand-point-right"></i>Your body can replenish that loss in a short time (a matter of hours for some components, a few weeks for others).<br>
<br>
</p>
<br>
<br>
<br>
<p style="color: blue;">I'm Afraid I'll Faint:<br></p>
<p>
<i class="far fa-hand-point-right"></i>During an AB Elite donation, you give plasma, a part of your blood used to treat patients in emergency situations.<br>
<i class="far fa-hand-point-right"></i>AB plasma can be given to anyone regardless of their blood type.<br>
</p>
